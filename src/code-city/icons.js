/**
 * Génère un élément SVG inline à partir d'un contenu de <path>.
 * @param {string} innerPath - contenu SVG (paths, circles, etc.)
 * @param {object} opts - options supplémentaires
 * @param {string} [opts.extra] - attributs supplémentaires sur le <svg>
 * @returns {string}
 */
function svg(innerPath, opts = {}) {
  return `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" ${opts.extra || ''}>${innerPath}</svg>`;
}

