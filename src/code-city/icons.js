/**
 * Renvoie le SVG inline pour un nom d'icône.
 * @param {string} name - nom de l'icône (ex: 'start', 'rocket', 'cloud')
 * @returns {string} SVG HTML string
 */
export function getIcon(name) {
  const fn = ICON_MAP[name];
  return fn ? fn() : iconCube();
}
